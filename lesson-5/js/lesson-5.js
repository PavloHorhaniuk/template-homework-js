const day = prompt("Введіть день тижня (наприклад: понеділок, вівторок тощо):").toLowerCase();
const workDays = ["понеділок", "вівторок", "середа", "четвер", "п’ятниця"];
const weekendDays = ["субота", "неділя"];

if (workDays.includes(day)) {
  console.log("Це робочий день.");
} else if (weekendDays.includes(day)) {
  console.log("Це вихідний.");
} else {
  console.log("Введено некоректний день.");
}

