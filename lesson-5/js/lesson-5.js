const drink = prompt("Оберіть напій: Кава, Чай або Сік").toLowerCase();
if (drink === "кава") {
  console.log("Ви обрали Кава!");
} else if (drink === "чай") {
  console.log("Ви обрали Чай!");
} else if (drink === "сік") {
  console.log("Ви обрали Сік!");
} else {
  console.log("Введено невірний вибір.");
}

