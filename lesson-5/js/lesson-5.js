const num1 = parseFloat(prompt("Введіть перше число:"));
const num2 = parseFloat(prompt("Введіть друге число:"));
const operation = prompt("Оберіть операцію (+, -, *, /):");

if (operation === "+") {
  console.log(`Результат: ${num1 + num2}`);
} else if (operation === "-") {
  console.log(`Результат: ${num1 - num2}`);
} else if (operation === "*") {
  console.log(`Результат: ${num1 * num2}`);
} else if (operation === "/") {
  if (num2 === 0) {
    console.error("Помилка: Ділення на нуль неможливе.");
  } else {
    console.log(`Результат: ${num1 / num2}`);
  }
} else {
  console.log("Некоректна операція.");
}
